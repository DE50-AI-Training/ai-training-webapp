import type { Metadata } from "next";
import localFont from "next/font/local";
import "./globals.css";
import Navbar from "@/components/navbar/Navbar";
import { Provider } from "jotai";
import HydrateAtoms from "./HydrateAtoms";
import { getModels } from "@/lib/services/models";
import { getDatasets } from "@/lib/services/dataset";
import { TooltipProvider } from "@/components/ui/Tooltip";

const geistSans = localFont({
    src: "./fonts/GeistVF.woff",
    variable: "--font-geist-sans",
    weight: "100 900",
});
const geistMono = localFont({
    src: "./fonts/GeistMonoVF.woff",
    variable: "--font-geist-mono",
    weight: "100 900",
});

export const metadata: Metadata = {
    title: "SimplifyAI",
    description: "Generated by create next app",
};

export default async function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    const models = await getModels();
    const datasets = await getDatasets();

    return (
        <Provider>
            <TooltipProvider>
                <HydrateAtoms initialModels={models} initialDatasets={datasets}>
                    <html lang="en">
                        <body
                            className={`${geistSans.variable} ${geistMono.variable} antialiased`}
                        >
                            <main className="mx-auto w-full lg:max-w-[90%] xl:max-w-[85%] 2xl:max-w-[80%] 3xl:max-w-[1536px] flex flex-col gap-12">
                                <Navbar />
                                {children}
                            </main>
                        </body>
                    </html>
                </HydrateAtoms>
            </TooltipProvider>
        </Provider>
    );
}
